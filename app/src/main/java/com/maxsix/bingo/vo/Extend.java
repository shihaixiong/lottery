public class Extend {
    private long win;
    private long lost;
    private long tie;
    private long pay;
    private long wait;
    private long fee;
    private long redraw;
    private long recharge;
    public long getWin() {
        return win;
    }

    public void setWin(long win) {
        this.win = win;
    }

    public long getLost() {
        return lost;
    }

    public void setLost(long lost) {
        this.lost = lost;
    }

    public long getTie() {
        return tie;
    }

    public void setTie(long tie) {
        this.tie = tie;
    }

    public long getPay() {
        return pay;
    }

    public void setPay(long pay) {
        this.pay = pay;
    }

    public long getWait() {
        return wait;
    }

    public void setWait(long wait) {
        this.wait = wait;
    }

    public long getFee() {
        return fee;
    }

    public void setFee(long fee) {
        this.fee = fee;
    }

    public long getRedraw() {
        return redraw;
    }

    public void setRedraw(long redraw) {
        this.redraw = redraw;
    }

    public long getRecharge() {
        return recharge;
    }

    public void setRecharge(long recharge) {
        this.recharge = recharge;
    }
}
